    /*
    |--------------------------------------------------------------------------
    | FUNCTIONS
    |--------------------------------------------------------------------------
    */
    public function getIsReadLabel()
    {
        return $this->is_read == 1 ? 'Прочитано' : 'Не прочитано';
    }

    public function read()
    {
      if ($this->is_read == 0) {
        $this->is_read = 1;
        $this->save();
      }
    }

